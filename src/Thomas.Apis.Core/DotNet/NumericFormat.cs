namespace Thomas.Apis.Core.DotNet
{
    /// <summary>
    /// The numeric format in which a value can be represented as string.
    /// </summary>
    public enum NumericFormat
    {
        /// <summary>
        /// Binary format.
        /// </summary>
        Binary = 2,

        /// <summary>
        /// Octal format;
        /// </summary>
        Octal = 8,

        /// <summary>
        /// Decimal format.
        /// </summary>
        Decimal = 10,

        /// <summary>
        /// Hexadecimal format.
        /// </summary>
        Hexadecimal = 16,
    }
}
